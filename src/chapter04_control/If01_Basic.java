package chapter04_control;

public class If01_Basic {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 1. 조건문이 필요한 이유
         * ==================================================
         *
         * 프로그램은 기본적으로
         * 위에서 아래로 한 줄씩 실행된다.
         *
         * 하지만 현실의 로직은 항상 "조건"이 존재한다.
         *
         * 예)
         * - 점수가 60점 이상이면 합격
         * - 나이가 19세 이상이면 성인
         *
         * 이러한 "조건에 따른 실행"을 처리하기 위해
         * if 문을 사용한다.
         */

        int score = 75;

        /*
         * ==================================================
         * 2. if 문 기본 구조
         * ==================================================
         *
         * if (조건식) {
         *     실행할 코드
         * }
         *
         * - 조건식이 true  → 블록 실행
         * - 조건식이 false → 블록 실행 안 함
         *
         * if 문은 "선택 구조"이며
         * 반복문이 아니다.
         */

        if (score >= 60) {
            System.out.println("합격입니다.");
        }

        /*
         * ==================================================
         * 3. 조건식은 반드시 boolean 이다
         * ==================================================
         *
         * if 괄호 안에는
         * 반드시 true 또는 false로
         * 평가될 수 있는 식만 들어갈 수 있다.
         *
         *  허용되는 조건식 예:
         *   score >= 60
         *   score < 100
         *   score == 75
         *
         *  허용되지 않는 예:
         *   if (score)       → 컴파일 에러
         *   if (10)          → 컴파일 에러
         */

        boolean isPass = score >= 60;

        if (isPass) {
            System.out.println("조건식은 boolean 변수도 가능");
        }

        /*
         * ==================================================
         * 4. 비교 연산자를 이용한 조건식
         * ==================================================
         *
         * >   : 초과
         * <   : 미만
         * >=  : 이상
         * <=  : 이하
         * ==  : 같다
         * !=  : 같지 않다
         *
         * 비교 연산자의 결과는 항상 boolean 이다.
         */

        int age = 20;

        if (age >= 19) {
            System.out.println("성인입니다.");
        }

        /*
         * ==================================================
         * 5. 논리 연산자를 이용한 조건 결합
         * ==================================================
         *
         * && : 그리고 (둘 다 true일 때 true)
         * || : 또는 (하나라도 true면 true)
         * !  : 부정 (true → false)
         */

        if (score >= 60 && age >= 19) {
            System.out.println("성인 + 합격 조건 만족");
        }

        /*
         * ==================================================
         * 6. 중괄호 생략에 대한 주의사항
         * ==================================================
         *
         * if 문에서 중괄호는 생략할 수 있지만
         * 초보 단계에서는 절대 권장하지 않는다.
         *
         * 중괄호가 없으면
         * if는 바로 아래 한 줄만 제어한다.
         */

        if (score >= 60)
            System.out.println("이 문장만 if의 영향을 받는다.");

        System.out.println("이 문장은 항상 실행된다.");

        /*
         * ==================================================
         * 7. if 문 실행 흐름 정리
         * ==================================================
         *
         * 1) 조건식 평가
         * 2) true  → 블록 실행
         * 3) false → 블록 건너뜀
         * 4) 다음 코드 계속 실행
         *
         * if 문은
         * 조건이 맞을 때 "한 번만" 실행된다.
         */

    }
}