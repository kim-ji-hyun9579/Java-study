package chapter04_control;

public class If02_IfElse {

    public static void main(String[] args) {

        /*
         * ===============================
         * 1. if - else 문이란?
         * ===============================
         *
         * 조건에 따라
         * 두 가지 중 하나를 반드시 선택해서
         * 실행하는 조건문
         *
         * if 문:
         * - 조건이 true일 때만 실행
         *
         * if - else 문:
         * - true면 if 실행
         * - false면 else 실행
         */

        int score = 55;

        /*
         * ===============================
         * 2. if - else 기본 구조
         * ===============================
         *
         * if (조건식) {
         *     조건이 true일 때 실행
         * } else {
         *     조건이 false일 때 실행
         * }
         */

        if (score >= 60) {
            System.out.println("합격입니다.");
        } else {
            System.out.println("불합격입니다.");
        }

        /*
         * ===============================
         * 3. if 단독 vs if - else 차이
         * ===============================
         *
         * if 단독:
         * - 조건이 false면 아무것도 실행 안 됨
         *
         * if - else:
         * - 둘 중 하나는 반드시 실행됨
         */

        int num = -3;

        if (num > 0) {
            System.out.println("양수");
        } else {
            System.out.println("0 또는 음수");
        }

        /*
         * ===============================
         * 4. 조건식은 if에만 작성한다
         * ===============================
         *
         * else에는 조건을 쓰지 않는다.
         *
         *  잘못된 예:
         * else (score < 60) { }
         *
         * else는
         * "if 조건이 거짓일 때"를 의미한다.
         */

        /*
         * ===============================
         * 5. 실행 흐름 정리
         * ===============================
         *
         * 1) if 조건식 평가
         * 2) true  → if 블록 실행, else는 건너뜀
         * 3) false → else 블록 실행, if는 건너뜀
         * 4) if - else 블록 종료 후 다음 코드 실행
         */


        /*
         * ===============================
         * 6. boolean 변수로 조건 분리
         * ===============================
         *
         * 조건식을 변수로 분리하면
         * 가독성과 유지보수가 좋아진다.
         */

        int age = 17;
        boolean isAdult = age >= 19;

        if (isAdult) {
            System.out.println("성인입니다.");
        } else {
            System.out.println("미성년자입니다.");
        }

        /*
         * ===============================
         * 7. 중괄호 생략 주의
         * ===============================
         *
         * if - else에서도
         * 중괄호를 생략할 수 있지만
         * 실수 가능성이 매우 크다.
         */

        if (score >= 60)
            System.out.println("합격");
        else
            System.out.println("불합격");

        /*
         * ===============================
         * 8. if - else 사용 시 주의점
         * ===============================
         *
         * - 조건은 반드시 boolean
         * - else는 if와 한 쌍
         * - 둘 중 하나는 반드시 실행
         * - 중괄호 생략은 지양
         */

        /*
         * ===============================
         * 9. if - else 요약
         * ===============================
         *
         * - 조건에 따라 둘 중 하나 선택
         * - true / false 흐름을 명확히 이해
         * - 실무에서도 가장 많이 쓰이는 구조
         */
    }
}