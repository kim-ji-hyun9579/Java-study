package chapter07_method;

public class Parameter02_Basic {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 1. 매개변수(Parameter)란?
         * ==================================================
         *
         * - 메서드 호출 시 값을 전달하기 위한 변수
         * - 메서드 선언부 () 안에 작성
         * - 호출할 때 실제 값(인자, argument)을 전달
         */

        /*
         * ==================================================
         * 2. 매개변수 1개 메서드
         * ==================================================
         */
        printNumber(10);
        printNumber(50);

        /*
         * ==================================================
         * 3. 매개변수 2개 메서드
         * ==================================================
         */
        add(3, 5);
        add(10, 20);

        /*
         * ==================================================
         * 4. 매개변수 + return
         * ==================================================
         */
        int result = multiply(4, 5);
        System.out.println("multiply 결과 : " + result);

        /*
         * ==================================================
         * 5. 매개변수 실행 흐름 정리
         * ==================================================
         *
         * main → 메서드 호출
         *     → 매개변수로 값 전달
         *     → 메서드 실행
         *     → return (있다면)
         */
    }

    /*
     * ==================================================
     * 2-1. 매개변수 1개
     * ==================================================
     */
    public static void printNumber(int num) {
        System.out.println("전달받은 값 : " + num);
    }

    /*
     * ==================================================
     * 3-1. 매개변수 2개
     * ==================================================
     */
    public static void add(int a, int b) {
        int sum = a + b;
        System.out.println("합계 : " + sum);
    }

    /*
     * ==================================================
     * 4-1. 매개변수 + return
     * ==================================================
     */
    public static int multiply(int x, int y) {
        return x * y;
    }
}