package chapter07_method;

public class Overloading04_Basic {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 1. void 메서드 vs return 메서드
         * ==================================================
         *
         * void   → 결과를 돌려주지 않음 (실행만)
         * return → 결과를 돌려줌
         */

        /*
         * ==================================================
         * 2. void 메서드 사용
         * ==================================================
         */
        printHello();
        printSum(3, 5);

        /*
         * ==================================================
         * 3. return 메서드 사용
         * ==================================================
         */
        int sum = getSum(10, 20);
        System.out.println("getSum 결과 : " + sum);

        /*
         * ==================================================
         * 4. void 메서드는 값 저장 불가
         * ==================================================
         *
         * 아래 코드는 컴파일 에러
         *
         * int result = printSum(1, 2);
         */

        /*
         * ==================================================
         * 5. return 메서드는 표현식으로 사용 가능
         * ==================================================
         */
        System.out.println("곱셈 결과 : " + multiply(4, 5));
    }

    /*
     * ==================================================
     * 2-1. void 메서드
     * ==================================================
     *
     * - 반환값 없음
     * - 실행만 담당
     */
    public static void printHello() {
        System.out.println("Hello!");
    }

    public static void printSum(int a, int b) {
        int sum = a + b;
        System.out.println("합계 : " + sum);
    }

    /*
     * ==================================================
     * 3-1. return 메서드
     * ==================================================
     *
     * - 결과를 호출한 곳으로 반환
     * - 반환 타입과 return 값 타입 일치 필수
     */
    public static int getSum(int a, int b) {
        return a + b;
    }

    /*
     * ==================================================
     * 3-2. return 즉시 종료
     * ==================================================
     */
    public static int multiply(int x, int y) {

        if (x == 0 || y == 0) {
            return 0;
        }

        return x * y;
    }
}