package chapter07_method;

public class Method01_Basic {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 1. 메서드란 무엇인가?
         * ==================================================
         *
         * - 특정 작업을 수행하는 코드 묶음
         * - 필요할 때 호출해서 사용
         * - 코드 중복 제거
         * - 가독성 + 유지보수 향상
         *
         * ※ main 메서드는
         *    프로그램 시작 지점이 되는 특수한 메서드
         */

        System.out.println("===== main 시작 =====");

        /*
         * ==================================================
         * 2. 반환값이 없는 메서드 (void)
         * ==================================================
         */
        sayHello();
        printLine();

        /*
         * ==================================================
         * 3. 반환값이 있는 메서드 (return)
         * ==================================================
         */
        int sum = addTwoNumbers();
        System.out.println("addTwoNumbers 결과 : " + sum);

        /*
         * ==================================================
         * 4. return의 역할
         * ==================================================
         *
         * - return을 만나면 메서드는 즉시 종료
         * - return 뒤 코드는 실행되지 않는다
         */
        int result = returnTest();
        System.out.println("returnTest 결과 : " + result);

        /*
         * ==================================================
         * 5. 메서드 실행 흐름 이해하기
         * ==================================================
         */
        flowTest();

        System.out.println("===== main 종료 =====");
    }

    /*
     * ==================================================
     * 2-1. 가장 기본적인 메서드
     * ==================================================
     */
    public static void sayHello() {
        System.out.println("안녕하세요!");
    }

    /*
     * ==================================================
     * 2-2. 출력만 담당하는 메서드
     * ==================================================
     */
    public static void printLine() {
        System.out.println("----------------------------");
    }

    /*
     * ==================================================
     * 3-1. 값을 반환하는 메서드
     * ==================================================
     */
    public static int addTwoNumbers() {
        int a = 10;
        int b = 20;
        return a + b;
    }

    /*
     * ==================================================
     * 4-1. return은 메서드를 즉시 종료한다
     * ==================================================
     */
    public static int returnTest() {

        System.out.println("returnTest 시작");

        if (true) {
            return 100;
        }

        // 이 코드는 절대 실행되지 않는다
        System.out.println("이 문장은 실행 안 됨");

        return 0;
    }

    /*
     * ==================================================
     * 5-1. 메서드 실행 흐름 테스트
     * ==================================================
     */
    public static void flowTest() {

        System.out.println("flowTest 시작");

        innerMethod();

        System.out.println("flowTest 종료");
    }

    /*
     * ==================================================
     * 5-2. 다른 메서드를 호출하는 메서드
     * ==================================================
     */
    public static void innerMethod() {
        System.out.println("innerMethod 실행");
    }
}