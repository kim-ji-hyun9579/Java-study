package chapter03_operator;

public class Operator01_Arithmetic {

    public static void main(String[] args) {

        /*
         * ===============================
         * 1. 산술 연산자란?
         * ===============================
         *
         * 숫자 값을 계산할 때 사용하는 연산자
         *
         * +  : 덧셈
         * -  : 뺄셈
         * *  : 곱셈
         * /  : 나눗셈 (몫)
         * %  : 나머지
         */

        int a = 10;
        int b = 3;

        /*
         * ===============================
         * 2. 기본 산술 연산
         * ===============================
         */

        System.out.println(a + b); // 13
        System.out.println(a - b); // 7
        System.out.println(a * b); // 30
        System.out.println(a / b); // 3 (정수 나눗셈)
        System.out.println(a % b); // 1 (나머지)

        /*
         * ===============================
         * 3. 정수 나눗셈 주의점
         * ===============================
         *
         * int / int → 결과는 int
         * 소수점은 버려진다.
         */

        int result1 = 10 / 4;
        System.out.println(result1); // 2

        /*
         * ===============================
         * 4. 실수 나눗셈
         * ===============================
         *
         * 피연산자 중 하나라도 실수면
         * 결과는 실수(double)
         */

        double result2 = 10 / 4.0;
        System.out.println(result2); // 2.5

        /*
         * ===============================
         * 5. 형 변환을 이용한 계산
         * ===============================
         */

        double result3 = (double) 10 / 4;
        System.out.println(result3); // 2.5

        /*
         * ===============================
         * 6. 문자열 + 숫자
         * ===============================
         *
         * + 연산자는
         * 문자열이 하나라도 포함되면
         * "문자열 연결"로 동작한다.
         */

        System.out.println("결과: " + a + b);   // 결과: 103
        System.out.println("결과: " + (a + b)); // 결과: 13

        /*
         * ===============================
         * 7. 산술 연산자 요약
         * ===============================
         *
         * - 정수끼리 나누면 결과는 정수
         * - 실수가 포함되면 결과는 실수
         * - %는 나머지를 구할 때 사용
         * - 문자열 + 숫자는 문자열 결합
         * - 연산 우선순위는 ()로 명확히
         */
    }
}