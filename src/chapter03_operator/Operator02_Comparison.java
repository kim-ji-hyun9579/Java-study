package chapter03_operator;

public class Operator02_Comparison {

    public static void main(String[] args) {

        /*
         * ===============================
         * 1. 비교 연산자란?
         * ===============================
         *
         * 두 값을 비교하여
         * 결과를 boolean(true / false)으로 반환하는 연산자
         *
         * >   : 초과
         * <   : 미만
         * >=  : 이상
         * <=  : 이하
         * ==  : 같다
         * !=  : 같지 않다
         */

        int a = 10;
        int b = 20;

        /*
         * ===============================
         * 2. 대소 비교
         * ===============================
         */

        System.out.println(a > b);   // false
        System.out.println(a < b);   // true
        System.out.println(a >= 10); // true
        System.out.println(b <= 20); // true

        /*
         * ===============================
         * 3. 같은지 / 다른지 비교
         * ===============================
         */

        System.out.println(a == b); // false
        System.out.println(a != b); // true

        /*
         * ===============================
         * 4. 비교 결과는 boolean
         * ===============================
         */

        boolean result1 = a < b;
        boolean result2 = a == 10;

        System.out.println(result1); // true
        System.out.println(result2); // true

        /*
         * ===============================
         * 5. if문과의 관계
         * ===============================
         *
         * 비교 연산자는
         * 조건문(if)의 핵심 재료다.
         */

        if (a < b) {
            System.out.println("a는 b보다 작다");
        }

        /*
         * ===============================
         * 6. 주의사항 ① = 와 == 차이
         * ===============================
         *
         * =  : 대입 연산자
         * == : 비교 연산자
         */

        int x = 5;
        // if (x = 10) {}  // 컴파일 에러 (boolean 아님)

        if (x == 5) {
            System.out.println("x는 5다");
        }

        /*
         * ===============================
         * 7. 주의사항 ② 실수 비교
         * ===============================
         *
         * 실수는 정확하게 떨어지지 않는 경우가 많다.
         */

        double d1 = 0.1 + 0.2;
        double d2 = 0.3;

        System.out.println(d1);        // 0.30000000000000004
        System.out.println(d1 == d2);  // false (주의!)

        /* 컴퓨터는 모든 숫자를 2진수로 저장하게 된다.
        *   0.1 (10진수) -> 0.00011001100110011...
        *   0.2 (10진수) -> 0.0011001100110011...
        *
        *   0.1 → 0.10000000000000000555...
        *   0.2 → 0.20000000000000001110...
        *
        *   0.1 + 0.2 = 0.30000000000000004...
        *
        *   선생님한테 금융권에서 BigDecimal로 계산하는 이유라고 들었던 적이 있다.
        * */

        /*
         * ===============================
         * 8. 비교 연산자 요약
         * ===============================
         *
         * - 비교 결과는 항상 boolean
         * - 조건문(if, while)의 핵심
         * - = 과 == 절대 헷갈리지 말 것
         * - 실수 비교는 == 사용 주의
         */
    }
}