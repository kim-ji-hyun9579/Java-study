package chapter02_variable;

public class Variable02_PrimitiveType {

    public static void main(String[] args) {

        /*
         * ===============================
         * 1. 기본 데이터 타입(Primitive Type)
         * ===============================
         *
         * - Java 에서 가장 기본이 되는 값 타입
         * - 변수에 "값 자체"를 저장한다
         * - Stack 메모리에 직접 저장됨
         * - 객체가 아님 (new 사용 X)
         */

        /*
         * ===============================
         * 2. 정수형 타입
         * ===============================
         */

        // 1byte (거의 사용 안 함)
        byte b = 10;

        // 2byte (거의 사용 안 함)
        short s = 100;

        // 4byte (정수형 기본 타입)
        int i = 1000;

        // 8byte (long은 L을 붙여야 함)
        long l = 10000000000L;

        /*
         * 정수형 타입은 기본적으로 int를 사용한다.
         * byte, short는 메모리 절약 목적이었지만
         * JVM에서는 int 단위로 연산되기 때문에 실무에서는 거의 사용하지 않는다.
         */


        /*
         * ===============================
         * 3. 실수형 타입
         * ===============================
         */

        // 4byte (정밀도 낮음, 사용 거의 안 함)
        float f = 3.14f;

        // 8byte (실수형 기본 타입)
        double d = 3.14;

        /*
         * 실수형은 기본적으로 double을 사용한다.
         * 실수는 부동소수점 방식이기 때문에 정확한 값이 아닐 수 있다.
         */


        /*
         * ===============================
         * 4. 문자형 타입
         * ===============================
         */

        // char는 2byte, 유니코드 기반
        char c1 = 'A';
        char c2 = '가';

        // 내부적으로는 숫자로 처리됨
        char c3 = 65; // 'A'

        System.out.println(c1);
        System.out.println(c3);


        /*
         * ===============================
         * 5. 논리형 타입
         * ===============================
         */

        boolean isAdult = true;
        boolean isLogin = false;

        /*
         * boolean은 조건문(if, while)에서 핵심적으로 사용된다.
         */


        /*
         * ===============================
         * 6. Primitive Type 정리
         * ===============================
         *
         * - 기본 타입은 Stack 메모리에 값 자체 저장
         * - null 사용 불가
         * - 연산 속도가 빠름
         * - 객체가 아니므로 메서드 없음
         *
         * 실무 기본 선택
         * - 정수 : int
         * - 실수 : double
         * - 문자 : char
         * - 논리 : boolean
         */
    }
}