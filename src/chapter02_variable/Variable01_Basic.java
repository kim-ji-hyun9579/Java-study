package chapter02_variable;

public class Variable01_Basic {

    public static void main(String[] args) {

        /*
         * ===============================
         * 1. 변수란 무엇인가?
         * ===============================
         *
         * 변수는 "값"이 아니라
         * 값을 저장할 수 있는 메모리 공간에 붙인 이름이다.
         *
         * Java에서 지역 변수는
         * main 메서드가 실행될 때 Stack 메모리에 생성된다.
         */

        /*
         * ===============================
         * 2. 변수 선언과 초기화
         * ===============================
         */

        // 선언: int 타입의 값을 저장할 수 있는 공간을 Stack에 확보
        int age;

        // 초기화: 확보된 Stack 공간에 값(20)을 처음 저장
        age = 20;

        // 재할당: 같은 공간에 값만 변경 (새 공간 생성 X)
        age = 30;

        System.out.println(age); // 30


        /*
         * ===============================
         * 3. 선언과 초기화를 동시에
         * ===============================
         */

        int height = 170;

        /*
         * ===============================
         * 4. Stack 메모리 관점
         * ===============================
         *
         * main 메서드 실행 시 Stack Frame 생성
         *
         * Stack Frame 내부
         * ┌─────────────┐
         * │ age    : 30 │
         * │ height :170 │
         * └─────────────┘
         *
         * 메서드 종료 시 Stack Frame 제거 → 변수 소멸
         */


        /*
         * ===============================
         * 5. Scope (변수의 생명 범위)
         * ===============================
         */

        {
            // 이 블록 안에서만 살아있는 지역 변수
            int temp = 100;
            System.out.println(temp);
        }

        // System.out.println(temp); // 컴파일 에러 (scope 종료)


        /*
         * ===============================
         * 6. 지역 변수는 초기화 필수
         * ===============================
         */

        // int value;
        // System.out.println(value); // 컴파일 에러
        //
        // 지역 변수는 자동 초기화되지 않음
        // → 개발자가 반드시 값의 책임을 져야 함


        /*
         * ===============================
         * 7. 상수 (final)
         * ===============================
         *
         * final이 붙은 변수는
         * 한 번만 값을 할당할 수 있다.
         */

        final int MAX_AGE = 100;
        // MAX_AGE = 200; // 컴파일 에러

        /*
         * ===============================
         * 8. 변수 요약
         * ===============================
         *
         * - 기본 타입 변수는 Stack 메모리에 저장
         * - 지역 변수는 scope를 벗어나면 즉시 소멸
         * - 초기화되지 않은 지역 변수는 사용 불가
         * - 값 변경은 기존 공간에 덮어쓰기
         * - final을 사용하면 상수 선언 가능
         */
    }
}