package chapter06_array;

public class Array03_TwoDimension {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 1. 2차원 배열이란?
         * ==================================================
         *
         * 2차원 배열은
         * "배열 안에 배열이 들어있는 구조"이다.
         *
         * 즉,
         * - 행(row)
         * - 열(column)
         * 로 구성된다.
         *
         * 예)
         * [
         *   [10, 20, 30],
         *   [40, 50, 60]
         * ]
         *
         * 행 index → 0, 1
         * 열 index → 0, 1, 2
         */

        /*
         * ==================================================
         * 2. 2차원 배열 선언
         * ==================================================
         *
         * 타입[][] 배열이름;
         */

        int[][] arr;

        /*
         * ==================================================
         * 3. 2차원 배열 생성
         * ==================================================
         *
         * new 타입[행][열]
         */

        arr = new int[2][3];   // 2행 3열

        /*
         * ==================================================
         * 4. 2차원 배열 구조 이해
         * ==================================================
         *
         * arr[0] → [ ?, ?, ? ]
         * arr[1] → [ ?, ?, ? ]
         *
         * arr[0][0] arr[0][1] arr[0][2]
         * arr[1][0] arr[1][1] arr[1][2]
         */

        /*
         * ==================================================
         * 5. 2차원 배열 값 저장
         * ==================================================
         */

        arr[0][0] = 10;
        arr[0][1] = 20;
        arr[0][2] = 30;

        arr[1][0] = 40;
        arr[1][1] = 50;
        arr[1][2] = 60;

        /*
         * ==================================================
         * 6. 2차원 배열 값 출력 (직접)
         * ==================================================
         */

        System.out.println(arr[0][0]);
        System.out.println(arr[0][1]);
        System.out.println(arr[0][2]);

        System.out.println(arr[1][0]);
        System.out.println(arr[1][1]);
        System.out.println(arr[1][2]);

        /*
         * ==================================================
         * 7. 2차원 배열과 중첩 for문 (핵심)
         * ==================================================
         *
         * 바깥 for → 행
         * 안쪽 for → 열
         */

        for (int i = 0; i < arr.length; i++) {          // 행 반복
            for (int j = 0; j < arr[i].length; j++) {   // 열 반복
                System.out.println("arr[" + i + "][" + j + "] = " + arr[i][j]);
            }
        }

        /*
         * ==================================================
         * 8. 2차원 배열 선언과 동시에 초기화
         * ==================================================
         */

        int[][] scores = {
                {90, 80, 70},
                {85, 95, 100},
                {60, 75, 88}
        };

        /*
         * scores 구조
         *
         * 90   80   70
         * 85   95  100
         * 60   75   88
         */

        for (int i = 0; i < scores.length; i++) {
            for (int j = 0; j < scores[i].length; j++) {
                System.out.print(scores[i][j] + " ");
            }
            System.out.println(); // 줄바꿈
        }

        /*
         * ==================================================
         * 9. 2차원 배열 핵심 요약
         * ==================================================
         *
         * - 배열 안에 배열 구조
         * - 행 / 열 개념 필수
         * - arr.length        → 행 개수
         * - arr[i].length     → 해당 행의 열 개수
         * - 중첩 for문 필수
         */

    }
}