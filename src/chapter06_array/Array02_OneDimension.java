package chapter06_array;

public class Array02_OneDimension {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 1. 1차원 배열이란?
         * ==================================================
         *
         * 1차원 배열은
         * 값을 "한 줄로" 순서대로 저장하는 배열이다.
         *
         * 예)
         * [10, 20, 30, 40, 50]
         *  0   1   2   3   4   ← index
         */

        /*
         * ==================================================
         * 2. 1차원 배열 선언 + 생성
         * ==================================================
         */

        int[] numbers = new int[5];

        /*
         * ==================================================
         * 3. 배열에 값 저장
         * ==================================================
         */

        numbers[0] = 10;
        numbers[1] = 20;
        numbers[2] = 30;
        numbers[3] = 40;
        numbers[4] = 50;

        /*
         * ==================================================
         * 4. 배열 값 출력 (직접 접근)
         * ==================================================
         */

        System.out.println(numbers[0]);
        System.out.println(numbers[1]);
        System.out.println(numbers[2]);
        System.out.println(numbers[3]);
        System.out.println(numbers[4]);

        /*
         * ==================================================
         * 5. 배열 길이 (length)
         * ==================================================
         *
         * 배열이 몇 칸인지 알고 싶을 때 사용
         */

        System.out.println("배열 길이 : " + numbers.length);

        /*
         * ==================================================
         * 6. 배열과 for 문 (핵심)
         * ==================================================
         *
         * index는 0부터
         * 마지막은 length - 1
         */

        for (int i = 0; i < numbers.length; i++) {
            System.out.println("numbers[" + i + "] = " + numbers[i]);
        }

        /*
         * ==================================================
         * 7. 배열 선언과 동시에 초기화
         * ==================================================
         */

        int[] scores = {90, 80, 70, 100};

        /*
         * scores → [90, 80, 70, 100]
         * index     0   1   2    3
         */

        for (int i = 0; i < scores.length; i++) {
            System.out.println("점수 : " + scores[i]);
        }

        /*
         * ==================================================
         * 8. 배열 기본값 (중요 개념)
         * ==================================================
         *
         * int     → 0
         * double  → 0.0
         * boolean → false
         * String  → null
         */

        int[] emptyArr = new int[3];

        System.out.println(emptyArr[0]); // 0
        System.out.println(emptyArr[1]); // 0
        System.out.println(emptyArr[2]); // 0

        /*
         * ==================================================
         * 9. 1차원 배열 핵심 요약
         * ==================================================
         *
         * - index는 0부터 시작
         * - 배열 길이는 length (는 매서드가 아니라, 프로퍼티 (이미 가지고 있는 값을 참조))
         * - for문과 반드시 같이 사용
         * - 선언 + 생성 + 초기화 방식 숙지
         */

    }
}