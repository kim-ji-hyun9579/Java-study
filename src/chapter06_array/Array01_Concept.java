package chapter06_array;

public class Array01_Concept {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 1. 배열이 필요한 이유
         * ==================================================
         *
         * 변수는 하나의 값만 저장할 수 있다.
         *
         * int score1 = 90;
         * int score2 = 80;
         * int score3 = 70;
         *
         * 만약 학생이 100명이면?
         * → 변수 100개 만들어야 함 (지옥)
         *
         * 이런 문제를 해결하기 위해
         * "배열(Array)"을 사용한다.
         */

        /*
         * ==================================================
         * 2. 배열이란?
         * ==================================================
         *
         * 배열은
         * "같은 타입의 여러 값을 하나로 묶어서 관리하는 공간"이다.
         *
         * - 같은 타입만 저장 가능
         * - 여러 개의 값을 순서대로 저장
         * - 각 값은 index(번호)로 구분
         */

        /*
         * ==================================================
         * 3. 배열 선언 기본 형태
         * ==================================================
         *
         * 타입[] 배열이름;
         *
         * 예)
         * int[] scores;
         * String[] names;
         */

        int[] scores;   // int 배열 선언

        /*
         * ==================================================
         * 4. 배열 생성 (메모리 할당)
         * ==================================================
         *
         * new 타입[크기]
         *
         * 크기 = 배열에 저장할 값의 개수
         */

        scores = new int[3];   // 정수 3개 저장 가능한 배열 생성

        /*
         * ==================================================
         * 5. 배열의 특징 (중요)
         * ==================================================
         *
         * ✔ 배열의 크기는 생성할 때 정해지면 변경 불가
         * ✔ 배열의 index는 0부터 시작
         * ✔ 마지막 index = 배열 크기 - 1
         *
         * scores → [ ?, ?, ? ]
         * index     0  1  2
         */

        /*
         * ==================================================
         * 6. 배열 값 저장
         * ==================================================
         */

        scores[0] = 90;
        scores[1] = 80;
        scores[2] = 70;

        /*
         * ==================================================
         * 7. 배열 값 출력
         * ==================================================
         */

        System.out.println(scores[0]);
        System.out.println(scores[1]);
        System.out.println(scores[2]);

        /*
         * ==================================================
         * 8. 배열 index 주의사항
         * ==================================================
         *
         * 존재하지 않는 index 접근 시
         * → ArrayIndexOutOfBoundsException 발생
         */

        // scores[3] = 100;  // 런타임 에러 (존재하지 않는 index)

        /*
         * ==================================================
         * 9. 배열 한 줄 요약
         * ==================================================
         *
         * - 같은 타입 여러 값을 저장하는 구조
         * - index는 0부터 시작
         * - 크기는 고정
         * - 반복문과 궁합이 미쳤다 (다음 단계)
         */

    }
}