package chapter06_array;

public class Array04_ArrayAndLoop {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 1. 배열과 반복문은 왜 항상 같이 쓰일까?
         * ==================================================
         *
         * 배열의 특징
         * - 값이 여러 개
         * - index가 0부터 순서대로 존재
         *
         * 반복문의 특징
         * - 같은 구조의 코드를 여러 번 실행
         *
         * → 배열의 모든 요소를
         *    "처음부터 끝까지" 처리하기에
         *    반복문이 가장 적합하다.
         */

        /*
         * ==================================================
         * 2. 배열을 반복문으로 순회하는 기본 구조
         * ==================================================
         *
         * for (int i = 0; i < 배열.length; i++) {
         *     배열[i] 사용
         * }
         */

        int[] arr = {10, 20, 30, 40, 50};

        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }

        /*
         * ==================================================
         * 3. 왜 i < 배열.length 인가?
         * ==================================================
         *
         * 배열 index
         * 0 ~ 배열.length - 1 까지 존재
         *
         * i <= 배열.length
         * → 마지막에 존재하지 않는 index 접근
         */

        /*
         * ==================================================
         * 4. 배열과 반복문의 역할 분리
         * ==================================================
         *
         * 배열  → 데이터 저장
         * 반복문 → 데이터 처리
         *
         * 이 역할 분리를 이해하면
         * 코드가 깔끔해진다.
         */

        /*
         * ==================================================
         * 5. 배열 값 읽기 (Read)
         * ==================================================
         */

        for (int i = 0; i < arr.length; i++) {
            int value = arr[i];   // 배열에서 값 읽기
            System.out.println("읽은 값 : " + value);
        }

        /*
         * ==================================================
         * 6. 배열 값 변경 (Write)
         * ==================================================
         *
         * 반복문을 이용하면
         * 배열 전체를 한 번에 수정 가능
         */

        for (int i = 0; i < arr.length; i++) {
            arr[i] = arr[i] * 2;
        }

        System.out.println("값 변경 후");

        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }

        /*
         * ==================================================
         * 7. 배열과 조건문 + 반복문
         * ==================================================
         *
         * 반복문 → 전체 순회
         * 조건문 → 필요한 값만 처리
         */

        for (int i = 0; i < arr.length; i++) {
            if (arr[i] >= 50) {
                System.out.println("50 이상 값 : " + arr[i]);
            }
        }

        /*
         * ==================================================
         * 8. 2차원 배열과 반복문 구조 복습
         * ==================================================
         *
         * for (행)
         *   for (열)
         */

        int[][] matrix = {
                {1, 2, 3},
                {4, 5, 6}
        };

        for (int i = 0; i < matrix.length; i++) {
            for (int j = 0; j < matrix[i].length; j++) {
                System.out.print(matrix[i][j] + " ");
            }
            System.out.println();
        }

        /*
         * ==================================================
         * 9. 배열 + 반복문 사고 방식 (중요)
         * ==================================================
         *
         * 1) 배열 크기 확인
         * 2) 반복문 범위 설정
         * 3) index로 값 접근
         * 4) 읽기 / 변경 / 조건 처리
         *
         * 이 순서로 생각하면
         * 어떤 배열 문제든 풀린다.
         */

        /*
         * ==================================================
         * 10. 배열과 반복문 핵심 요약
         * ==================================================
         *
         * - 배열은 혼자 쓰지 않는다
         * - 반복문 없이는 배열을 제대로 다룰 수 없다
         * - for문 + length는 세트
         * - 읽기 / 변경 / 조건 처리 패턴 숙지
         */

    }
}