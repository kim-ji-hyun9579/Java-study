package chapter05_loop;

public class While01_Basic {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 1. while 문이 필요한 이유
         * ==================================================
         *
         * 반복 횟수가 정해져 있지 않거나
         * "조건이 만족하는 동안" 계속 반복해야 할 때
         * while 문을 사용한다.
         *
         * 예)
         * - 비밀번호가 맞을 때까지 입력
         * - 체력이 0이 될 때까지 공격
         */

        /*
         * ==================================================
         * 2. while 문 기본 구조
         * ==================================================
         *
         * while (조건식) {
         *     반복 실행할 코드
         * }
         *
         * - 조건식이 true  → 반복 실행
         * - 조건식이 false → 반복 종료
         */

        int i = 1;

        while (i <= 5) {
            System.out.println("while문 실행 : " + i);
            i++;
        }

        /*
         * ==================================================
         * 3. while 문 실행 순서
         * ==================================================
         *
         * 1) 조건식 검사
         * 2) true  → 블록 실행
         * 3) 증감식 실행
         * 4) 다시 조건식 검사
         *
         * ※ for 문과 달리
         *    초기식과 증감식을
         *    개발자가 직접 관리해야 한다.
         */

        /*
         * ==================================================
         * 4. 무한 루프 주의
         * ==================================================
         *
         * 증감식이 없으면
         * 조건이 영원히 true가 되어
         * 무한 루프가 발생한다.
         */

        int count = 1;

        // while (count <= 5) {
        //     System.out.println(count);
        // }   // 무한 루프

        /*
         * ==================================================
         * 5. 0부터 시작하는 while 문
         * ==================================================
         *
         * for 문과 동일하게
         * 0부터 시작하는 패턴도 자주 사용된다.
         */

        int num = 0;

        while (num < 5) {
            System.out.println("0부터 시작 : " + num);
            num++;
        }

        /*
         * ==================================================
         * 6. while 문 vs for 문
         * ==================================================
         *
         * for   → 반복 횟수가 명확할 때
         * while → 조건 중심 반복일 때
         *
         * 둘 다 기능은 같지만
         * 상황에 따라 가독성이 달라진다.
         */

        /*
         * ==================================================
         * 7. while 문 핵심 요약
         * ==================================================
         *
         * - 조건이 true인 동안 반복
         * - 조건식이 먼저 실행됨
         * - 증감식 누락 시 무한 루프 주의
         * - 조건 기반 반복에 적합
         */

    }
}