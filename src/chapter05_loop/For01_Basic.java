package chapter05_loop;

public class For01_Basic {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 1. 반복문이 필요한 이유
         * ==================================================
         *
         * 같은 코드를 여러 번 실행해야 할 때
         * 매번 직접 작성하는 것은 비효율적이다.
         *
         * 예)
         * System.out.println("안녕하세요");
         * System.out.println("안녕하세요");
         * System.out.println("안녕하세요");
         *
         * 이런 경우 반복문을 사용한다.
         */

        /*
         * ==================================================
         * 2. for 문 기본 구조
         * ==================================================
         *
         * for (초기식; 조건식; 증감식) {
         *     반복 실행할 코드
         * }
         *
         * - 초기식 : 반복을 시작할 변수 선언
         * - 조건식 : 반복을 계속할지 판단
         * - 증감식 : 변수 값을 증가/감소
         */

        for (int i = 1; i <= 5; i++) {
            System.out.println("for문 실행 : " + i);
        }

        /*
         * ==================================================
         * 3. for 문 실행 순서
         * ==================================================
         *
         * 1) 초기식 실행 (딱 1번)
         * 2) 조건식 검사
         *    true  → 블록 실행
         *    false → 반복 종료
         * 3) 블록 실행 후 증감식 실행
         * 4) 다시 조건식 검사
         */

        /*
         * ==================================================
         * 4. 반복 횟수 이해하기
         * ==================================================
         *
         * i = 1부터 시작해서
         * i <= 5 인 동안 반복
         *
         * → 총 5번 반복
         */

        /*
         * ==================================================
         * 5. 변수 i는 반복문 전용 변수
         * ==================================================
         *
         * for 문 안에서 선언된 변수는
         * for 문이 끝나면 사라진다.
         */

        // System.out.println(i);  // 컴파일 에러 발생

        /*
         * ==================================================
         * 6. 0부터 시작하는 for 문 (가장 많이 쓰임)
         * ==================================================
         *
         * 실무와 배열에서는
         * 0부터 시작하는 패턴이 기본이다.
         */

        for (int i = 0; i < 5; i++) {
            System.out.println("0부터 시작 : " + i);
        }

        /*
         * ==================================================
         * 7. 증가값을 바꾼 for 문
         * ==================================================
         *
         * i += 2  → 2씩 증가
         */

        for (int i = 0; i <= 10; i += 2) {
            System.out.println("짝수 출력 : " + i);
        }

        /*
         * ==================================================
         * 8. for 문 핵심 요약
         * ==================================================
         *
         * - 반복 횟수가 명확할 때 사용
         * - 조건이 false가 되면 즉시 종료
         * - 초기식은 한 번만 실행
         * - 조건식은 매 반복마다 검사
         */

    }
}