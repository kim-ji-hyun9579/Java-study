package chapter05_loop;

public class BreakContinue01_Basic {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 1. break 문이 필요한 이유
         * ==================================================
         *
         * 반복문을
         * "조건이 만족되면 즉시 종료"하고 싶을 때
         * break 를 사용한다.
         *
         * → 반복문 탈출
         */

        /*
         * ==================================================
         * 2. break 기본 사용법
         * ==================================================
         */

        for (int i = 1; i <= 10; i++) {

            if (i == 5) {
                break;   // 반복문 즉시 종료
            }

            System.out.println("break 전 i = " + i);
        }

        /*
         * 실행 결과:
         * 1
         * 2
         * 3
         * 4
         */

        /*
         * ==================================================
         * 3. while 문에서 break
         * ==================================================
         */

        int num = 1;

        while (true) {

            if (num == 4) {
                break;
            }

            System.out.println("while break : " + num);
            num++;
        }

        /*
         * ==================================================
         * 4. continue 문이 필요한 이유
         * ==================================================
         *
         * 반복문을 종료하지 않고
         * "현재 반복만 건너뛰고"
         * 다음 반복으로 넘어가고 싶을 때 사용
         */

        /*
         * ==================================================
         * 5. continue 기본 사용법
         * ==================================================
         */

        for (int i = 1; i <= 5; i++) {

            if (i == 3) {
                continue;   // 아래 코드 스킵
            }

            System.out.println("continue i = " + i);
        }

        /*
         * 실행 결과:
         * 1
         * 2
         * 4
         * 5
         */

        /*
         * ==================================================
         * 6. continue 실행 흐름
         * ==================================================
         *
         * continue를 만나면
         * - 현재 반복 즉시 종료
         * - 증감식으로 이동
         * - 조건식 다시 검사
         */

        /*
         * ==================================================
         * 7. while 문에서 continue 주의
         * ==================================================
         *
         * continue 전에
         * 증감식이 없으면
         * 무한 루프 발생
         */

        int i = 0;

        while (i < 5) {

            i++;

            if (i == 2) {
                continue;
            }

            System.out.println("while continue : " + i);
        }

        /*
         * ==================================================
         * 8. break vs continue 정리
         * ==================================================
         *
         * break    → 반복문 완전 종료
         * continue → 현재 반복만 스킵
         */

    }
}