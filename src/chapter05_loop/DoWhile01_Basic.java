package chapter05_loop;

public class DoWhile01_Basic {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 1. do-while 문이 필요한 이유
         * ==================================================
         *
         * 조건과 상관없이
         * "무조건 한 번은 실행"해야 하는 경우가 있다.
         *
         * 예)
         * - 메뉴를 최소 한 번 출력
         * - 사용자 입력을 먼저 받고 검사
         */

        /*
         * ==================================================
         * 2. do-while 문 기본 구조
         * ==================================================
         *
         * do {
         *     반복 실행할 코드
         * } while (조건식);
         *
         * ※ while 뒤에 반드시 세미콜론(;)이 필요하다.
         */

        int i = 1;

        do {
            System.out.println("do-while 실행 : " + i);
            i++;
        } while (i <= 5);

        /*
         * ==================================================
         * 3. do-while 실행 순서
         * ==================================================
         *
         * 1) do 블록 먼저 실행
         * 2) 조건식 검사
         * 3) true  → 다시 do 실행
         * 4) false → 반복 종료
         *
         * → 최초 1회는 조건 검사 없이 실행됨
         */

        /*
         * ==================================================
         * 4. while 문과 차이점
         * ==================================================
         *
         * while :
         *   조건 먼저 검사 → 실행 여부 결정
         *
         * do-while :
         *   실행 먼저 → 조건 검사
         */

        int num = 10;

        while (num < 5) {
            System.out.println("while 실행");
        }

        do {
            System.out.println("do-while 실행 (조건 false여도 실행됨)");
        } while (num < 5);

        /*
         * ==================================================
         * 5. do-while 활용 예시 (메뉴 출력)
         * ==================================================
         *
         * 메뉴는
         * 최소 한 번은 출력되어야 한다.
         */

        int menu = 0;

        do {
            System.out.println("===== 메뉴 =====");
            System.out.println("1. 등록");
            System.out.println("2. 조회");
            System.out.println("3. 종료");
            System.out.println("================");

            menu++;   // 입력받았다고 가정

        } while (menu != 3);

        /*
         * ==================================================
         * 6. do-while 핵심 요약
         * ==================================================
         *
         * - 무조건 1번은 실행됨
         * - 조건 검사는 나중에
         * - 메뉴 / 입력 처리에 자주 사용
         * - while 뒤 세미콜론 필수
         */

    }
}