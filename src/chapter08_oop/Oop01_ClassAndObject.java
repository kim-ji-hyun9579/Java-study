package chapter08_oop;

/*
 * ===============================
 * 1. 클래스와 객체
 * ===============================
 *
 * 클래스: 설계도
 * 객체: 설계도로 만든 실제 물건
 */

class Student {

    /*
     * ===============================
     * 2. 필드(멤버변수)
     * ===============================
     * 객체가 가지는 "상태/정보"
     */

    String name;
    int age;

    /*
     * ===============================
     * 3. 메서드(기능)
     * ===============================
     * 객체가 할 수 있는 "동작"
     */

    void introduce() {
        System.out.println("이름: " + name);
        System.out.println("나이: " + age);
    }
}

public class Oop01_ClassAndObject {

    public static void main(String[] args) {

        /*
         * ===============================
         * 4. 객체 생성 (인스턴스 생성)
         * ===============================
         * new Student() -> Student 설계도로 실제 객체를 만든다.
         */

        Student s1 = new Student();

        /*
         * ===============================
         * 5. 객체의 필드에 값 넣기
         * ===============================
         * 객체명.필드명
         */

        s1.name = "홍길동";
        s1.age = 20;

        /*
         * ===============================
         * 6. 객체의 메서드 호출
         * ===============================
         * 객체명.메서드명()
         */

        s1.introduce();

        System.out.println("------");

        /*
         * ===============================
         * 7. 객체는 여러 개 만들 수 있음
         * ===============================
         * 같은 클래스(설계도)로도
         * 서로 다른 객체를 만들 수 있다.
         */

        Student s2 = new Student();
        s2.name = "이순신";
        s2.age = 25;

        s2.introduce();

        System.out.println("------");

        Student s3 = new Student();
        s3.introduce(); // 값 안넣으면 기본값 출력됨

        /*
         * ===============================
         * 8. 필드의 기본값
         * ===============================
         *
         * String -> null
         * int -> 0
         * double -> 0.0
         * boolean -> false
         *
         * 값을 넣지 않아도 기본값이 자동으로 들어간다.
         */
    }
}