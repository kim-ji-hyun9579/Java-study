package chapter08_oop;

/*
 * ==================================================
 * 1. this 키워드란?
 * ==================================================
 *
 * this = "현재 객체 자신"
 *
 * 쉽게 말하면:
 * → 지금 이 객체를 가리키는 참조변수
 *
 * 언제 쓰는가?
 * → 필드(멤버변수)와 매개변수 이름이 같을 때
 */

class Student3 {

    /*
     * ==================================================
     * 2. 필드 (멤버 변수)
     * ==================================================
     */

    String name;
    int age;

    /*
     * ==================================================
     * 3. 매개변수 생성자
     * ==================================================
     */

    Student3(String name, int age) {

        /*
         * 문제 상황
         *
         * name = name;
         * age = age;
         *
         * 이 코드의 의미는?
         *
         * → 매개변수 name에
         *   매개변수 name을 대입
         *
         * 즉, 필드는 건드리지 않음.
         */

        this.name = name;
        this.age = age;

        /*
         * this.name → 현재 객체의 필드
         * name      → 매개변수
         */
    }

    void introduce() {
        System.out.println("이름: " + name);
        System.out.println("나이: " + age);
    }
}

public class Oop03_ThisKeyword {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 4. 객체 생성 흐름
         * ==================================================
         */

        Student3 s1 = new Student3("홍길동", 20);
        s1.introduce();
    }
}