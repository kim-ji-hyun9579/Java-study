package chapter08_oop;

/*
 * ==================================================
 * 1. 변수의 종류
 * ==================================================
 *
 * 자바 변수는 3가지로 나뉜다.
 *
 * 1. 지역 변수 (Local Variable)
 * 2. 멤버 변수 (Instance Variable)
 * 3. 클래스 변수 (Static Variable)
 */

class Counter {

    /*
     * ==================================================
     * 2. 멤버 변수 (Instance Variable)
     * ==================================================
     *
     * - 클래스 안
     * - 메서드 밖
     * - 객체마다 따로 존재
     * - Heap 메모리에 저장
     */

    int count = 0;

    /*
     * ==================================================
     * 3. 클래스 변수 (Static Variable)
     * ==================================================
     *
     * - static 키워드 사용
     * - 모든 객체가 공유
     * - Method Area(공통 영역)에 저장
     */

    static int totalCount = 0;

    /*
     * ==================================================
     * 4. 메서드
     * ==================================================
     */

    void increase() {

        /*
         * ==================================================
         * 5. 지역 변수 (Local Variable)
         * ==================================================
         *
         * - 메서드 안에서 선언
         * - 실행될 때만 존재
         * - Stack 메모리에 저장
         * - 반드시 초기화해야 사용 가능
         */

        int localValue = 1;

        count += localValue;
        totalCount += localValue;

        System.out.println("현재 객체 count: " + count);
        System.out.println("전체 객체 totalCount: " + totalCount);
    }
}

public class Oop04_VariableType {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 6. 객체 생성
         * ==================================================
         */

        Counter c1 = new Counter();
        Counter c2 = new Counter();

        /*
         * ==================================================
         * 7. 실행 흐름 이해
         * ==================================================
         */

        c1.increase();
        System.out.println("------");

        c2.increase();
    }
}