package chapter08_oop;

/*
 * ==================================================
 * 1. 생성자(Constructor)란?
 * ==================================================
 *
 * 객체가 생성되는 순간 자동으로 실행되는 특별한 메서드
 *
 * new Student() 하는 순간 실행됨
 *
 * 목적:
 * → 객체를 "초기화" 하기 위해 사용
 *
 * 특징:
 * 1. 클래스 이름과 동일하다.
 * 2. 리턴 타입이 없다. (void도 쓰지 않는다)
 * 3. 객체 생성 시 자동 실행된다.
 */

class Student2 {

    /*
     * ==================================================
     * 2. 필드 (멤버 변수)
     * ==================================================
     * 객체가 가지는 상태(정보)
     */

    String name;
    int age;

    /*
     * ==================================================
     * 3. 생성자를 만들지 않으면?
     * ==================================================
     *
     * 자바가 자동으로 기본 생성자를 만들어준다.
     *
     * Student2() { }
     *
     * 하지만!
     * 우리가 생성자를 하나라도 만들면
     * 자동 기본 생성자는 사라진다.
     */

    /*
     * ==================================================
     * 4. 기본 생성자
     * ==================================================
     * 매개변수가 없는 생성자
     */

    Student2() {
        System.out.println("기본 생성자 실행됨");
    }

    /*
     * ==================================================
     * 5. 매개변수 생성자
     * ==================================================
     *
     * 객체 생성과 동시에 값을 넣고 싶을 때 사용
     *
     * new Student2("홍길동", 20);
     */

    Student2(String name, int age) {

        System.out.println("매개변수 생성자 실행됨");

        /*
         * this는 현재 객체 자신을 의미한다.
         *
         * this.name → 현재 객체의 필드
         * name      → 매개변수
         */

        this.name = name;
        this.age = age;
    }

    /*
     * ==================================================
     * 6. 메서드
     * ==================================================
     */

    void introduce() {
        System.out.println("이름: " + name);
        System.out.println("나이: " + age);
    }
}

public class Oop02_Constructor {

    public static void main(String[] args) {

        /*
         * ==================================================
         * 7. 객체 생성 흐름 이해하기
         * ==================================================
         *
         * Student2 s1 = new Student2();
         *
         * 1단계: 메모리에 Student2 객체 생성
         * 2단계: 생성자 실행
         * 3단계: s1이 객체를 참조
         */

        Student2 s1 = new Student2();
        s1.name = "홍길동";
        s1.age = 20;
        s1.introduce();

        System.out.println("------");

        /*
         * ==================================================
         * 8. 매개변수 생성자 사용 흐름
         * ==================================================
         *
         * Student2 s2 = new Student2("이순신", 25);
         *
         * 1단계: 객체 생성
         * 2단계: 매개변수 생성자 실행
         * 3단계: 필드에 값 자동 세팅
         *
         * ※ 생성자 없이 만들게 되면
         *    객체가 불완전한 상태 일 수 있기 때문
         */

        Student2 s2 = new Student2("이순신", 25);
        s2.introduce();
    }
}